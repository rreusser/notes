program test_zlarz
  use test_utils
  implicit none

  complex*16 :: v(10), C(30), work(10), tau
  double precision :: C_real(60)
  equivalence (C, C_real)

  ! Test 1: Left side, M=4, N=3, L=2
  ! H applied to last L=2 rows of the M=4 rows.
  v(1) = (0.5d0, 0.2d0)
  v(2) = (-0.3d0, 0.4d0)
  tau = (1.2d0, -0.4d0)
  ! Build 4x3 col-major matrix
  C(1) = (1.0d0, 0.0d0)
  C(2) = (2.0d0, 1.0d0)
  C(3) = (3.0d0, -1.0d0)
  C(4) = (4.0d0, 0.5d0)
  C(5) = (-1.0d0, 2.0d0)
  C(6) = (0.5d0, 0.5d0)
  C(7) = (1.5d0, -0.5d0)
  C(8) = (-2.0d0, 1.0d0)
  C(9) = (0.0d0, 1.0d0)
  C(10) = (1.0d0, 1.0d0)
  C(11) = (-0.5d0, 0.0d0)
  C(12) = (2.0d0, -2.0d0)
  work = (0.0d0, 0.0d0)
  call zlarz('L', 4, 3, 2, v, 1, tau, C, 4, work)
  call begin_test('zlarz_left_4x3_l2')
  call print_array('C', C_real, 24)
  call end_test()

  ! Test 2: Right side, M=3, N=4, L=2
  v(1) = (0.5d0, 0.2d0)
  v(2) = (-0.3d0, 0.4d0)
  tau = (0.8d0, 0.5d0)
  C(1) = (1.0d0, 0.0d0)
  C(2) = (2.0d0, 1.0d0)
  C(3) = (3.0d0, -1.0d0)
  C(4) = (4.0d0, 0.5d0)
  C(5) = (-1.0d0, 2.0d0)
  C(6) = (0.5d0, 0.5d0)
  C(7) = (1.5d0, -0.5d0)
  C(8) = (-2.0d0, 1.0d0)
  C(9) = (0.0d0, 1.0d0)
  C(10) = (1.0d0, 1.0d0)
  C(11) = (-0.5d0, 0.0d0)
  C(12) = (2.0d0, -2.0d0)
  work = (0.0d0, 0.0d0)
  call zlarz('R', 3, 4, 2, v, 1, tau, C, 3, work)
  call begin_test('zlarz_right_3x4_l2')
  call print_array('C', C_real, 24)
  call end_test()

  ! Test 3: tau = 0 (no-op)
  v(1) = (0.5d0, 0.2d0)
  v(2) = (-0.3d0, 0.4d0)
  tau = (0.0d0, 0.0d0)
  C(1) = (1.0d0, 0.0d0)
  C(2) = (2.0d0, 1.0d0)
  C(3) = (3.0d0, -1.0d0)
  C(4) = (4.0d0, 0.5d0)
  C(5) = (-1.0d0, 2.0d0)
  C(6) = (0.5d0, 0.5d0)
  work = (0.0d0, 0.0d0)
  call zlarz('L', 3, 2, 2, v, 1, tau, C, 3, work)
  call begin_test('zlarz_tau_zero')
  call print_array('C', C_real, 12)
  call end_test()

  ! Test 4: L=0 (trivial reflector, only identity row affected)
  tau = (1.0d0, 0.3d0)
  C(1) = (1.0d0, 0.0d0)
  C(2) = (2.0d0, 1.0d0)
  C(3) = (3.0d0, -1.0d0)
  C(4) = (4.0d0, 0.5d0)
  C(5) = (-1.0d0, 2.0d0)
  C(6) = (0.5d0, 0.5d0)
  work = (0.0d0, 0.0d0)
  call zlarz('L', 3, 2, 0, v, 1, tau, C, 3, work)
  call begin_test('zlarz_l_zero_left')
  call print_array('C', C_real, 12)
  call end_test()

  ! Test 5: Left side, M=3, N=2, L=3 (L == M, all rows meaningful)
  v(1) = (0.5d0, 0.2d0)
  v(2) = (-0.3d0, 0.4d0)
  v(3) = (0.7d0, -0.1d0)
  tau = (0.9d0, 0.2d0)
  C(1) = (1.0d0, 0.0d0)
  C(2) = (2.0d0, 1.0d0)
  C(3) = (3.0d0, -1.0d0)
  C(4) = (4.0d0, 0.5d0)
  C(5) = (-1.0d0, 2.0d0)
  C(6) = (0.5d0, 0.5d0)
  work = (0.0d0, 0.0d0)
  call zlarz('L', 3, 2, 3, v, 1, tau, C, 3, work)
  call begin_test('zlarz_left_3x2_l3')
  call print_array('C', C_real, 12)
  call end_test()

  ! Test 6: Right side, M=2, N=3, L=3 (L == N)
  v(1) = (0.5d0, 0.2d0)
  v(2) = (-0.3d0, 0.4d0)
  v(3) = (0.7d0, -0.1d0)
  tau = (1.1d0, -0.3d0)
  C(1) = (1.0d0, 0.0d0)
  C(2) = (2.0d0, 1.0d0)
  C(3) = (3.0d0, -1.0d0)
  C(4) = (4.0d0, 0.5d0)
  C(5) = (-1.0d0, 2.0d0)
  C(6) = (0.5d0, 0.5d0)
  work = (0.0d0, 0.0d0)
  call zlarz('R', 2, 3, 3, v, 1, tau, C, 2, work)
  call begin_test('zlarz_right_2x3_l3')
  call print_array('C', C_real, 12)
  call end_test()

end program
