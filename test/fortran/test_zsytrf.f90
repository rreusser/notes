program test_zsytrf
  use test_utils
  implicit none

  integer, parameter :: NMAX = 6
  complex*16 :: A(NMAX, NMAX), B(NMAX, 2), WORK(NMAX*32)
  double precision :: A_r(2*NMAX*NMAX), B_r(2*NMAX*2)
  equivalence (A, A_r)
  equivalence (B, B_r)
  integer :: IPIV(NMAX), INFO, n, LWORK

  ! Test 1: Upper, 4x4 blocked
  n = 4
  LWORK = NMAX * 32
  A = (0.0d0, 0.0d0)
  A(1,1) = (2.0d0, 1.0d0)
  A(1,2) = (1.0d0, 2.0d0)
  A(1,3) = (3.0d0, -1.0d0)
  A(1,4) = (0.5d0, 0.5d0)
  A(2,2) = (5.0d0, -1.0d0)
  A(2,3) = (2.0d0, 1.0d0)
  A(2,4) = (1.0d0, -2.0d0)
  A(3,3) = (4.0d0, 2.0d0)
  A(3,4) = (3.0d0, 0.0d0)
  A(4,4) = (6.0d0, -3.0d0)
  call ZSYTRF('U', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('upper_4x4')
  call print_int('n', n)
  call print_int('info', INFO)
  call print_array('A', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call end_test()

  ! Test 2: Lower, 4x4 blocked
  n = 4
  A = (0.0d0, 0.0d0)
  A(1,1) = (2.0d0, 1.0d0)
  A(2,1) = (1.0d0, 2.0d0)
  A(2,2) = (5.0d0, -1.0d0)
  A(3,1) = (3.0d0, -1.0d0)
  A(3,2) = (2.0d0, 1.0d0)
  A(3,3) = (4.0d0, 2.0d0)
  A(4,1) = (0.5d0, 0.5d0)
  A(4,2) = (1.0d0, -2.0d0)
  A(4,3) = (3.0d0, 0.0d0)
  A(4,4) = (6.0d0, -3.0d0)
  call ZSYTRF('L', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('lower_4x4')
  call print_int('n', n)
  call print_int('info', INFO)
  call print_array('A', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call end_test()

  ! Test 3: N=0
  call ZSYTRF('U', 0, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('n0')
  call print_int('info', INFO)
  call end_test()

  ! Test 4: Factor+Solve round-trip, upper, 4x4
  n = 4
  A = (0.0d0, 0.0d0)
  A(1,1) = (2.0d0, 1.0d0)
  A(1,2) = (1.0d0, 2.0d0)
  A(1,3) = (3.0d0, -1.0d0)
  A(1,4) = (0.5d0, 0.5d0)
  A(2,2) = (5.0d0, -1.0d0)
  A(2,3) = (2.0d0, 1.0d0)
  A(2,4) = (1.0d0, -2.0d0)
  A(3,3) = (4.0d0, 2.0d0)
  A(3,4) = (3.0d0, 0.0d0)
  A(4,4) = (6.0d0, -3.0d0)
  ! Make B = known RHS
  B(1,1) = (1.0d0, 0.0d0)
  B(2,1) = (0.0d0, 1.0d0)
  B(3,1) = (1.0d0, -1.0d0)
  B(4,1) = (2.0d0, 0.5d0)
  B(1,2) = (0.0d0, 0.0d0)
  B(2,2) = (1.0d0, 0.0d0)
  B(3,2) = (0.0d0, 0.0d0)
  B(4,2) = (0.0d0, 0.0d0)
  call ZSYTRF('U', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call ZSYTRS('U', n, 2, A, NMAX, IPIV, B, NMAX, INFO)
  call begin_test('roundtrip_upper')
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*2)
  call end_test()

  ! Test 5: Factor+Solve round-trip, lower, 4x4
  n = 4
  A = (0.0d0, 0.0d0)
  A(1,1) = (2.0d0, 1.0d0)
  A(2,1) = (1.0d0, 2.0d0)
  A(2,2) = (5.0d0, -1.0d0)
  A(3,1) = (3.0d0, -1.0d0)
  A(3,2) = (2.0d0, 1.0d0)
  A(3,3) = (4.0d0, 2.0d0)
  A(4,1) = (0.5d0, 0.5d0)
  A(4,2) = (1.0d0, -2.0d0)
  A(4,3) = (3.0d0, 0.0d0)
  A(4,4) = (6.0d0, -3.0d0)
  B(1,1) = (1.0d0, 0.0d0)
  B(2,1) = (0.0d0, 1.0d0)
  B(3,1) = (1.0d0, -1.0d0)
  B(4,1) = (2.0d0, 0.5d0)
  B(1,2) = (0.0d0, 0.0d0)
  B(2,2) = (1.0d0, 0.0d0)
  B(3,2) = (0.0d0, 0.0d0)
  B(4,2) = (0.0d0, 0.0d0)
  call ZSYTRF('L', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call ZSYTRS('L', n, 2, A, NMAX, IPIV, B, NMAX, INFO)
  call begin_test('roundtrip_lower')
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*2)
  call end_test()

  ! Test 6: Lower, 6x6 - force 2x2 pivoting with small diagonals
  n = 6
  A = (0.0d0, 0.0d0)
  A(1,1) = (0.01d0, 0.0d0)
  A(2,1) = (5.0d0, 1.0d0)
  A(2,2) = (0.02d0, 0.0d0)
  A(3,1) = (1.0d0, -1.0d0)
  A(3,2) = (2.0d0, 1.0d0)
  A(3,3) = (8.0d0, -2.0d0)
  A(4,1) = (0.5d0, 0.5d0)
  A(4,2) = (1.0d0, -1.0d0)
  A(4,3) = (3.0d0, 0.0d0)
  A(4,4) = (7.0d0, 1.0d0)
  A(5,1) = (2.0d0, 0.0d0)
  A(5,2) = (1.5d0, 0.5d0)
  A(5,3) = (0.0d0, 2.0d0)
  A(5,4) = (1.0d0, -0.5d0)
  A(5,5) = (6.0d0, 0.0d0)
  A(6,1) = (1.0d0, 1.0d0)
  A(6,2) = (0.0d0, 3.0d0)
  A(6,3) = (1.0d0, 0.0d0)
  A(6,4) = (2.0d0, 2.0d0)
  A(6,5) = (0.5d0, -1.0d0)
  A(6,6) = (5.0d0, -1.0d0)
  call ZSYTRF('L', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('lower_6x6')
  call print_int('n', n)
  call print_int('info', INFO)
  call print_array('A', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call end_test()

end program
