program test_zhetd2
  use test_utils
  implicit none

  integer :: INFO
  complex*16 :: A4(4,4), A1(1,1), TAU(4)
  double precision :: A4_r(32), A1_r(2), TAU_r(8)
  double precision :: D(4), E(4)
  equivalence (A4, A4_r)
  equivalence (A1, A1_r)
  equivalence (TAU, TAU_r)

  ! Test 1: UPLO='U', 4x4 Hermitian matrix
  ! H = [ 4,       1+i,    2-i,    0.5+0.5i ]
  !     [ 1-i,     5,      1+2i,   1-i      ]
  !     [ 2+i,     1-2i,   6,      2+i      ]
  !     [ 0.5-0.5i,1+i,    2-i,    7        ]
  A4(1,1) = dcmplx(4.0d0, 0.0d0)
  A4(1,2) = dcmplx(1.0d0, 1.0d0)
  A4(1,3) = dcmplx(2.0d0, -1.0d0)
  A4(1,4) = dcmplx(0.5d0, 0.5d0)
  A4(2,1) = dcmplx(1.0d0, -1.0d0)
  A4(2,2) = dcmplx(5.0d0, 0.0d0)
  A4(2,3) = dcmplx(1.0d0, 2.0d0)
  A4(2,4) = dcmplx(1.0d0, -1.0d0)
  A4(3,1) = dcmplx(2.0d0, 1.0d0)
  A4(3,2) = dcmplx(1.0d0, -2.0d0)
  A4(3,3) = dcmplx(6.0d0, 0.0d0)
  A4(3,4) = dcmplx(2.0d0, 1.0d0)
  A4(4,1) = dcmplx(0.5d0, -0.5d0)
  A4(4,2) = dcmplx(1.0d0, 1.0d0)
  A4(4,3) = dcmplx(2.0d0, -1.0d0)
  A4(4,4) = dcmplx(7.0d0, 0.0d0)
  D = 0.0d0; E = 0.0d0; TAU = dcmplx(0.0d0, 0.0d0)
  call ZHETD2('U', 4, A4, 4, D, E, TAU, INFO)
  call begin_test('upper_4x4')
  call print_int('info', INFO)
  call print_array('A', A4_r, 32)
  call print_array('d', D, 4)
  call print_array('e', E, 3)
  call print_array('tau', TAU_r, 6)
  call end_test()

  ! Test 2: UPLO='L', 4x4 same Hermitian matrix
  A4(1,1) = dcmplx(4.0d0, 0.0d0)
  A4(1,2) = dcmplx(1.0d0, 1.0d0)
  A4(1,3) = dcmplx(2.0d0, -1.0d0)
  A4(1,4) = dcmplx(0.5d0, 0.5d0)
  A4(2,1) = dcmplx(1.0d0, -1.0d0)
  A4(2,2) = dcmplx(5.0d0, 0.0d0)
  A4(2,3) = dcmplx(1.0d0, 2.0d0)
  A4(2,4) = dcmplx(1.0d0, -1.0d0)
  A4(3,1) = dcmplx(2.0d0, 1.0d0)
  A4(3,2) = dcmplx(1.0d0, -2.0d0)
  A4(3,3) = dcmplx(6.0d0, 0.0d0)
  A4(3,4) = dcmplx(2.0d0, 1.0d0)
  A4(4,1) = dcmplx(0.5d0, -0.5d0)
  A4(4,2) = dcmplx(1.0d0, 1.0d0)
  A4(4,3) = dcmplx(2.0d0, -1.0d0)
  A4(4,4) = dcmplx(7.0d0, 0.0d0)
  D = 0.0d0; E = 0.0d0; TAU = dcmplx(0.0d0, 0.0d0)
  call ZHETD2('L', 4, A4, 4, D, E, TAU, INFO)
  call begin_test('lower_4x4')
  call print_int('info', INFO)
  call print_array('A', A4_r, 32)
  call print_array('d', D, 4)
  call print_array('e', E, 3)
  call print_array('tau', TAU_r, 6)
  call end_test()

  ! Test 3: N=1 upper
  A1(1,1) = dcmplx(3.0d0, 0.0d0)
  D(1) = 0.0d0
  call ZHETD2('U', 1, A1, 1, D, E, TAU, INFO)
  call begin_test('n_one_upper')
  call print_int('info', INFO)
  call print_array('A', A1_r, 2)
  call print_scalar('d1', D(1))
  call end_test()

  ! Test 4: N=1 lower
  A1(1,1) = dcmplx(5.0d0, 0.0d0)
  D(1) = 0.0d0
  call ZHETD2('L', 1, A1, 1, D, E, TAU, INFO)
  call begin_test('n_one_lower')
  call print_int('info', INFO)
  call print_array('A', A1_r, 2)
  call print_scalar('d1', D(1))
  call end_test()

  ! Test 5: N=0 quick return
  call ZHETD2('U', 0, A1, 1, D, E, TAU, INFO)
  call begin_test('n_zero')
  call print_int('info', INFO)
  call end_test()

  ! Test 6: UPLO='U', 3x3 diagonal Hermitian (tau=0 path)
  A4(1,1) = dcmplx(2.0d0, 0.0d0)
  A4(1,2) = dcmplx(0.0d0, 0.0d0)
  A4(1,3) = dcmplx(0.0d0, 0.0d0)
  A4(2,1) = dcmplx(0.0d0, 0.0d0)
  A4(2,2) = dcmplx(5.0d0, 0.0d0)
  A4(2,3) = dcmplx(0.0d0, 0.0d0)
  A4(3,1) = dcmplx(0.0d0, 0.0d0)
  A4(3,2) = dcmplx(0.0d0, 0.0d0)
  A4(3,3) = dcmplx(8.0d0, 0.0d0)
  D = 0.0d0; E = 0.0d0; TAU = dcmplx(0.0d0, 0.0d0)
  call ZHETD2('U', 3, A4, 4, D, E, TAU, INFO)
  call begin_test('upper_diagonal')
  call print_int('info', INFO)
  call print_array('d', D, 3)
  call print_array('e', E, 2)
  call print_array('tau', TAU_r, 4)
  call end_test()

  ! Test 7: UPLO='L', 3x3 diagonal Hermitian (tau=0 path)
  A4(1,1) = dcmplx(2.0d0, 0.0d0)
  A4(1,2) = dcmplx(0.0d0, 0.0d0)
  A4(1,3) = dcmplx(0.0d0, 0.0d0)
  A4(2,1) = dcmplx(0.0d0, 0.0d0)
  A4(2,2) = dcmplx(5.0d0, 0.0d0)
  A4(2,3) = dcmplx(0.0d0, 0.0d0)
  A4(3,1) = dcmplx(0.0d0, 0.0d0)
  A4(3,2) = dcmplx(0.0d0, 0.0d0)
  A4(3,3) = dcmplx(8.0d0, 0.0d0)
  D = 0.0d0; E = 0.0d0; TAU = dcmplx(0.0d0, 0.0d0)
  call ZHETD2('L', 3, A4, 4, D, E, TAU, INFO)
  call begin_test('lower_diagonal')
  call print_int('info', INFO)
  call print_array('d', D, 3)
  call print_array('e', E, 2)
  call print_array('tau', TAU_r, 4)
  call end_test()

end program
