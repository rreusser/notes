program test_zsytrs_rook
  use test_utils
  implicit none

  integer, parameter :: NMAX = 6
  complex*16 :: A(NMAX, NMAX), B(NMAX, 3), WORK(NMAX*32)
  double precision :: A_r(2*NMAX*NMAX), B_r(2*NMAX*3)
  equivalence (A, A_r)
  equivalence (B, B_r)
  integer :: IPIV(NMAX), INFO, n, LWORK, nrhs

  LWORK = NMAX * 32

  ! Test 1: Upper, 4x4, 1 RHS - complex symmetric
  n = 4
  nrhs = 1
  A = (0.0d0, 0.0d0)
  A(1,1) = (2.0d0, 1.0d0)
  A(1,2) = (1.0d0, 2.0d0)
  A(1,3) = (3.0d0, -1.0d0)
  A(1,4) = (0.5d0, 0.5d0)
  A(2,2) = (5.0d0, -1.0d0)
  A(2,3) = (2.0d0, 1.0d0)
  A(2,4) = (1.0d0, -2.0d0)
  A(3,3) = (4.0d0, 2.0d0)
  A(3,4) = (3.0d0, 0.0d0)
  A(4,4) = (6.0d0, -3.0d0)
  B = (0.0d0, 0.0d0)
  B(1,1) = (1.0d0, 0.0d0)
  B(2,1) = (0.0d0, 1.0d0)
  B(3,1) = (1.0d0, -1.0d0)
  B(4,1) = (2.0d0, 0.5d0)
  call ZSYTRF_ROOK('U', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('upper_4x4_1rhs')
  call print_int('n', n)
  call print_int('nrhs', nrhs)
  call print_int('lda', NMAX)
  call print_int('info_trf', INFO)
  call print_array('A_factored', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call print_array('b', B_r, 2*NMAX*nrhs)
  call ZSYTRS_ROOK('U', n, nrhs, A, NMAX, IPIV, B, NMAX, INFO)
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*nrhs)
  call end_test()

  ! Test 2: Lower, 4x4, 2 RHS - complex symmetric
  n = 4
  nrhs = 2
  A = (0.0d0, 0.0d0)
  A(1,1) = (2.0d0, 1.0d0)
  A(2,1) = (1.0d0, 2.0d0)
  A(2,2) = (5.0d0, -1.0d0)
  A(3,1) = (3.0d0, -1.0d0)
  A(3,2) = (2.0d0, 1.0d0)
  A(3,3) = (4.0d0, 2.0d0)
  A(4,1) = (0.5d0, 0.5d0)
  A(4,2) = (1.0d0, -2.0d0)
  A(4,3) = (3.0d0, 0.0d0)
  A(4,4) = (6.0d0, -3.0d0)
  B = (0.0d0, 0.0d0)
  B(1,1) = (1.0d0, 0.0d0)
  B(2,1) = (0.0d0, 1.0d0)
  B(3,1) = (1.0d0, -1.0d0)
  B(4,1) = (2.0d0, 0.5d0)
  B(1,2) = (0.5d0, -0.5d0)
  B(2,2) = (1.0d0, 1.0d0)
  B(3,2) = (-1.0d0, 0.0d0)
  B(4,2) = (0.0d0, 2.0d0)
  call ZSYTRF_ROOK('L', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('lower_4x4_2rhs')
  call print_int('n', n)
  call print_int('nrhs', nrhs)
  call print_int('lda', NMAX)
  call print_int('info_trf', INFO)
  call print_array('A_factored', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call print_array('b', B_r, 2*NMAX*nrhs)
  call ZSYTRS_ROOK('L', n, nrhs, A, NMAX, IPIV, B, NMAX, INFO)
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*nrhs)
  call end_test()

  ! Test 3: N=0
  call ZSYTRS_ROOK('U', 0, 1, A, NMAX, IPIV, B, NMAX, INFO)
  call begin_test('n0')
  call print_int('info', INFO)
  call end_test()

  ! Test 4: N=1
  n = 1
  nrhs = 1
  A(1,1) = (3.0d0, 2.0d0)
  B(1,1) = (1.0d0, 1.0d0)
  call ZSYTRF_ROOK('U', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('n1')
  call print_int('n', n)
  call print_int('nrhs', nrhs)
  call print_int('lda', NMAX)
  call print_int('info_trf', INFO)
  call print_array('A_factored', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call print_array('b', B_r, 2*NMAX*nrhs)
  call ZSYTRS_ROOK('U', n, nrhs, A, NMAX, IPIV, B, NMAX, INFO)
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*nrhs)
  call end_test()

  ! Test 5: Lower, 6x6 forced 2x2 pivots (small diagonal)
  n = 6
  nrhs = 1
  A = (0.0d0, 0.0d0)
  A(1,1) = (0.01d0, 0.0d0)
  A(2,1) = (5.0d0, -1.0d0)
  A(2,2) = (0.02d0, 0.0d0)
  A(3,1) = (1.0d0, 1.0d0)
  A(3,2) = (2.0d0, -1.0d0)
  A(3,3) = (8.0d0, 1.0d0)
  A(4,1) = (0.5d0, -0.5d0)
  A(4,2) = (1.0d0, 1.0d0)
  A(4,3) = (3.0d0, 0.5d0)
  A(4,4) = (7.0d0, -1.0d0)
  A(5,1) = (2.0d0, 0.0d0)
  A(5,2) = (1.5d0, -0.5d0)
  A(5,3) = (0.0d0, -2.0d0)
  A(5,4) = (1.0d0, 0.5d0)
  A(5,5) = (6.0d0, 2.0d0)
  A(6,1) = (1.0d0, -1.0d0)
  A(6,2) = (0.0d0, -3.0d0)
  A(6,3) = (1.0d0, 0.0d0)
  A(6,4) = (2.0d0, -2.0d0)
  A(6,5) = (0.5d0, 1.0d0)
  A(6,6) = (5.0d0, -2.0d0)
  B = (0.0d0, 0.0d0)
  B(1,1) = (1.0d0, 0.0d0)
  B(2,1) = (0.0d0, 1.0d0)
  B(3,1) = (2.0d0, -1.0d0)
  B(4,1) = (1.0d0, 1.0d0)
  B(5,1) = (-1.0d0, 0.5d0)
  B(6,1) = (0.5d0, -0.5d0)
  call ZSYTRF_ROOK('L', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('lower_6x6')
  call print_int('n', n)
  call print_int('nrhs', nrhs)
  call print_int('lda', NMAX)
  call print_int('info_trf', INFO)
  call print_array('A_factored', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call print_array('b', B_r, 2*NMAX*nrhs)
  call ZSYTRS_ROOK('L', n, nrhs, A, NMAX, IPIV, B, NMAX, INFO)
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*nrhs)
  call end_test()

  ! Test 6: Upper, 6x6 forced 2x2 pivots
  n = 6
  nrhs = 1
  A = (0.0d0, 0.0d0)
  A(1,1) = (5.0d0, -2.0d0)
  A(1,2) = (0.5d0, 1.0d0)
  A(1,3) = (1.0d0, 0.5d0)
  A(1,4) = (2.0d0, -2.0d0)
  A(1,5) = (0.0d0, -3.0d0)
  A(1,6) = (1.0d0, -1.0d0)
  A(2,2) = (6.0d0, 2.0d0)
  A(2,3) = (1.5d0, 0.5d0)
  A(2,4) = (0.0d0, 2.0d0)
  A(2,5) = (1.0d0, -0.5d0)
  A(2,6) = (2.0d0, 0.0d0)
  A(3,3) = (7.0d0, -1.0d0)
  A(3,4) = (3.0d0, 0.5d0)
  A(3,5) = (1.0d0, -1.0d0)
  A(3,6) = (0.5d0, 0.5d0)
  A(4,4) = (8.0d0, 1.0d0)
  A(4,5) = (2.0d0, 1.0d0)
  A(4,6) = (1.0d0, 2.0d0)
  A(5,5) = (0.02d0, 0.0d0)
  A(5,6) = (5.0d0, -1.0d0)
  A(6,6) = (0.01d0, 0.0d0)
  B = (0.0d0, 0.0d0)
  B(1,1) = (1.0d0, 0.0d0)
  B(2,1) = (0.0d0, 1.0d0)
  B(3,1) = (2.0d0, -1.0d0)
  B(4,1) = (1.0d0, 1.0d0)
  B(5,1) = (-1.0d0, 0.5d0)
  B(6,1) = (0.5d0, -0.5d0)
  call ZSYTRF_ROOK('U', n, A, NMAX, IPIV, WORK, LWORK, INFO)
  call begin_test('upper_6x6')
  call print_int('n', n)
  call print_int('nrhs', nrhs)
  call print_int('lda', NMAX)
  call print_int('info_trf', INFO)
  call print_array('A_factored', A_r, 2*NMAX*n)
  call print_int_array('ipiv', IPIV, n)
  call print_array('b', B_r, 2*NMAX*nrhs)
  call ZSYTRS_ROOK('U', n, nrhs, A, NMAX, IPIV, B, NMAX, INFO)
  call print_int('info', INFO)
  call print_array('x', B_r, 2*NMAX*nrhs)
  call end_test()

end program
