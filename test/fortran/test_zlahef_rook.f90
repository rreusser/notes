  call end_test()

  ! Test 8: Upper 5x5 chase, NB=3 partial panel
  A5 = (0.0d0, 0.0d0)
  A5(1,1) = (0.1d0, 0.0d0)
  A5(1,2) = (1.0d0, -0.5d0)
  A5(2,2) = (0.1d0, 0.0d0)
  A5(1,3) = (2.0d0, 1.0d0)
  A5(2,3) = (5.0d0, -1.0d0)
  A5(3,3) = (0.1d0, 0.0d0)
  A5(1,4) = (3.0d0, -0.5d0)
  A5(2,4) = (10.0d0, 2.0d0)
  A5(3,4) = (50.0d0, -1.0d0)
  A5(4,4) = (0.1d0, 0.0d0)
  A5(1,5) = (4.0d0, 0.3d0)
  A5(2,5) = (20.0d0, -1.0d0)
  A5(3,5) = (100.0d0, -0.5d0)
  A5(4,5) = (500.0d0, 1.0d0)
  A5(5,5) = (0.1d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('U', 5, 3, kb, A5, 5, IPIV, W5_3, 5, INFO)
  call begin_test('upper_5x5_chase_nb3')
