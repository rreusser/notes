  A6(4,1) = (0.5d0, -0.5d0)
  A6(4,2) = (1.0d0, 1.0d0)
  A6(4,3) = (3.0d0, 0.0d0)
  A6(4,4) = (7.0d0, 0.0d0)
  A6(5,1) = (2.0d0, 0.0d0)
  A6(5,2) = (1.5d0, -0.5d0)
  A6(5,3) = (0.0d0, 2.0d0)
  A6(5,4) = (1.0d0, 0.5d0)
  A6(5,5) = (6.0d0, 0.0d0)
  A6(6,1) = (1.0d0, -1.0d0)
  A6(6,2) = (0.0d0, -3.0d0)
  A6(6,3) = (1.0d0, 0.0d0)
  A6(6,4) = (2.0d0, -2.0d0)
  A6(6,5) = (0.5d0, 1.0d0)
  A6(6,6) = (5.0d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('L', 6, 3, kb, A6, 6, IPIV, W6, 6, INFO)
  call begin_test('lower_6x6_nb3')
  call print_int('n', 6)
  call print_int('nb', 3)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A6_r, 72)
  call print_int_array('ipiv', IPIV, 6)
  call end_test()

  ! Test 2: Upper, 6x6 Hermitian, NB=3
  A6 = (0.0d0, 0.0d0)
  A6(1,1) = (0.01d0, 0.0d0)
  A6(1,2) = (5.0d0, 1.0d0)
  A6(2,2) = (0.02d0, 0.0d0)
  A6(1,3) = (1.0d0, -1.0d0)
  A6(2,3) = (2.0d0, 1.0d0)
  A6(3,3) = (8.0d0, 0.0d0)
  A6(1,4) = (0.5d0, 0.5d0)
  A6(2,4) = (1.0d0, -1.0d0)
  A6(3,4) = (3.0d0, 0.0d0)
  A6(4,4) = (7.0d0, 0.0d0)
  A6(1,5) = (2.0d0, 0.0d0)
  A6(2,5) = (1.5d0, 0.5d0)
  A6(3,5) = (0.0d0, -2.0d0)
  A6(4,5) = (1.0d0, -0.5d0)
  A6(5,5) = (6.0d0, 0.0d0)
  A6(1,6) = (1.0d0, 1.0d0)
  A6(2,6) = (0.0d0, 3.0d0)
  A6(3,6) = (1.0d0, 0.0d0)
  A6(4,6) = (2.0d0, 2.0d0)
  A6(5,6) = (0.5d0, -1.0d0)
  A6(6,6) = (5.0d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('U', 6, 3, kb, A6, 6, IPIV, W6, 6, INFO)
  call begin_test('upper_6x6_nb3')
  call print_int('n', 6)
  call print_int('nb', 3)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A6_r, 72)
  call print_int_array('ipiv', IPIV, 6)
  call end_test()

  ! Test 3: Lower, 4x4 Hermitian, NB=8 (full factorization, well-conditioned)
  A4 = (0.0d0, 0.0d0)
  A4(1,1) = (4.0d0, 0.0d0)
  A4(2,1) = (1.0d0, 0.5d0)
  A4(2,2) = (3.0d0, 0.0d0)
  A4(3,1) = (2.0d0, -1.0d0)
  A4(3,2) = (0.5d0, -0.2d0)
  A4(3,3) = (5.0d0, 0.0d0)
  A4(4,1) = (0.5d0, 0.1d0)
  A4(4,2) = (1.0d0, 0.3d0)
  A4(4,3) = (0.2d0, -0.4d0)
  A4(4,4) = (6.0d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('L', 4, 8, kb, A4, 4, IPIV, W4, 4, INFO)
  call begin_test('lower_4x4_full_nb8')
  call print_int('n', 4)
  call print_int('nb', 8)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A4_r, 32)
  call print_int_array('ipiv', IPIV, 4)
  call end_test()

  ! Test 4: Upper, 4x4 Hermitian, NB=8 (full factorization)
  A4 = (0.0d0, 0.0d0)
  A4(1,1) = (4.0d0, 0.0d0)
  A4(1,2) = (1.0d0, -0.5d0)
  A4(2,2) = (3.0d0, 0.0d0)
  A4(1,3) = (2.0d0, 1.0d0)
  A4(2,3) = (0.5d0, 0.2d0)
  A4(3,3) = (5.0d0, 0.0d0)
  A4(1,4) = (0.5d0, -0.1d0)
  A4(2,4) = (1.0d0, -0.3d0)
  A4(3,4) = (0.2d0, 0.4d0)
  A4(4,4) = (6.0d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('U', 4, 8, kb, A4, 4, IPIV, W4, 4, INFO)
  call begin_test('upper_4x4_full_nb8')
  call print_int('n', 4)
  call print_int('nb', 8)
  call print_int('kb', kb)
  call print_int('info', INFO)
