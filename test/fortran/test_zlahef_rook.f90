  A5 = (0.0d0, 0.0d0)
  A5(1,1) = (0.1d0, 0.0d0)
  A5(2,1) = (1.0d0, 0.5d0)
  A5(2,2) = (0.1d0, 0.0d0)
  A5(3,1) = (2.0d0, -1.0d0)
  A5(3,2) = (5.0d0, 1.0d0)
  A5(3,3) = (0.1d0, 0.0d0)
  A5(4,1) = (3.0d0, 0.5d0)
  A5(4,2) = (10.0d0, -2.0d0)
  A5(4,3) = (50.0d0, 1.0d0)
  A5(4,4) = (0.1d0, 0.0d0)
  A5(5,1) = (4.0d0, -0.3d0)
  A5(5,2) = (20.0d0, 1.0d0)
  A5(5,3) = (100.0d0, 0.5d0)
  A5(5,4) = (500.0d0, -1.0d0)
  A5(5,5) = (0.1d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('L', 5, 3, kb, A5, 5, IPIV, W5_3, 5, INFO)
  call begin_test('lower_5x5_chase_nb3')
