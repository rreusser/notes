  A5(3,2) = (5.0d0, 1.0d0)
  A5(3,3) = (0.1d0, 0.0d0)
  A5(4,1) = (3.0d0, 0.5d0)
  A5(4,2) = (10.0d0, -2.0d0)
  A5(4,3) = (50.0d0, 1.0d0)
  A5(4,4) = (0.1d0, 0.0d0)
  A5(5,1) = (4.0d0, -0.3d0)
  A5(5,2) = (20.0d0, 1.0d0)
  A5(5,3) = (100.0d0, 0.5d0)
  A5(5,4) = (500.0d0, -1.0d0)
  A5(5,5) = (0.1d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('L', 5, 8, kb, A5, 5, IPIV, W5, 5, INFO)
  call begin_test('lower_5x5_chase_full')
  call print_int('n', 5)
  call print_int('nb', 8)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A5_r, 50)
  call print_int_array('ipiv', IPIV, 5)
  call end_test()

  ! Test 6: Upper 5x5 with rook chase
  A5 = (0.0d0, 0.0d0)
  A5(1,1) = (0.1d0, 0.0d0)
  A5(1,2) = (1.0d0, -0.5d0)
  A5(2,2) = (0.1d0, 0.0d0)
  A5(1,3) = (2.0d0, 1.0d0)
  A5(2,3) = (5.0d0, -1.0d0)
  A5(3,3) = (0.1d0, 0.0d0)
  A5(1,4) = (3.0d0, -0.5d0)
  A5(2,4) = (10.0d0, 2.0d0)
  A5(3,4) = (50.0d0, -1.0d0)
  A5(4,4) = (0.1d0, 0.0d0)
  A5(1,5) = (4.0d0, 0.3d0)
  A5(2,5) = (20.0d0, -1.0d0)
  A5(3,5) = (100.0d0, -0.5d0)
  A5(4,5) = (500.0d0, 1.0d0)
  A5(5,5) = (0.1d0, 0.0d0)
  IPIV = 0
  call ZLAHEF_ROOK('U', 5, 8, kb, A5, 5, IPIV, W5, 5, INFO)
