  implicit none

  complex*16 :: A6(6, 6), W6(6, 3)
  double precision :: A6_r(72)
  equivalence (A6, A6_r)
  complex*16 :: A4(4, 4), W4(4, 4)
  double precision :: A4_r(32)
  equivalence (A4, A4_r)
  complex*16 :: A5(5, 5), W5(5, 5), W5_3(5, 3)
  double precision :: A5_r(50)
  equivalence (A5, A5_r)
  integer :: IPIV(6), INFO, kb

  ! Test 1: Lower, 6x6 Hermitian, NB=3 (partial panel)
  A6 = (0.0d0, 0.0d0)
  A6(1,1) = (0.01d0, 0.0d0)
  A6(2,1) = (5.0d0, -1.0d0)
  A6(2,2) = (0.02d0, 0.0d0)
  A6(3,1) = (1.0d0, 1.0d0)
  A6(3,2) = (2.0d0, -1.0d0)
