program test_zlaunhr_col_getrfnp2
  use test_utils
  implicit none
  double precision :: a_r(200)
  complex*16 :: a(100)
  equivalence (a, a_r)
  double precision :: d_r(40)
  complex*16 :: d(20)
  equivalence (d, d_r)
  integer :: info

  ! Test 1: 3x3 matrix
  a = (0.0d0, 0.0d0)
  d = (0.0d0, 0.0d0)
  a(1) = (0.5d0, 0.3d0); a(2) = (0.2d0, -0.1d0); a(3) = (-0.3d0, 0.2d0)
  a(4) = (-0.1d0, 0.4d0); a(5) = (0.6d0, -0.2d0); a(6) = (0.1d0, 0.3d0)
  a(7) = (0.2d0, -0.3d0); a(8) = (-0.4d0, 0.1d0); a(9) = (0.5d0, 0.2d0)
  call zlaunhr_col_getrfnp2(3, 3, a, 3, d, info)
  call begin_test('3x3')
  call print_array('a', a_r, 18)
  call print_array('d', d_r, 6)
  call print_int('info', info)
  call end_test()

  ! Test 2: 4x3 tall matrix (M > N)
  a = (0.0d0, 0.0d0)
  d = (0.0d0, 0.0d0)
  a(1) = (0.3d0, 0.2d0); a(2) = (0.1d0, -0.4d0); a(3) = (-0.2d0, 0.3d0); a(4) = (0.4d0, 0.1d0)
  a(5) = (-0.1d0, 0.5d0); a(6) = (0.5d0, -0.2d0); a(7) = (0.2d0, 0.3d0); a(8) = (-0.3d0, 0.1d0)
  a(9) = (0.2d0, -0.2d0); a(10) = (-0.4d0, 0.2d0); a(11) = (0.6d0, 0.1d0); a(12) = (0.1d0, -0.3d0)
  call zlaunhr_col_getrfnp2(4, 3, a, 4, d, info)
  call begin_test('4x3')
  call print_array('a', a_r, 24)
  call print_array('d', d_r, 6)
  call print_int('info', info)
  call end_test()

  ! Test 3: 3x4 wide matrix (M < N)
  a = (0.0d0, 0.0d0)
  d = (0.0d0, 0.0d0)
  a(1) = (0.4d0, 0.2d0); a(2) = (0.1d0, -0.3d0); a(3) = (-0.2d0, 0.4d0)
  a(4) = (-0.1d0, 0.3d0); a(5) = (0.5d0, -0.2d0); a(6) = (0.2d0, 0.1d0)
  a(7) = (0.2d0, -0.1d0); a(8) = (-0.3d0, 0.2d0); a(9) = (0.6d0, 0.3d0)
  a(10) = (0.1d0, 0.4d0); a(11) = (-0.2d0, -0.1d0); a(12) = (0.3d0, 0.2d0)
  call zlaunhr_col_getrfnp2(3, 4, a, 3, d, info)
  call begin_test('3x4')
  call print_array('a', a_r, 24)
  call print_array('d', d_r, 6)
  call print_int('info', info)
  call end_test()

  ! Test 4: 1x1 matrix (M=1 branch)
  a(1) = (0.7d0, 0.3d0)
  d(1) = (0.0d0, 0.0d0)
  call zlaunhr_col_getrfnp2(1, 1, a, 1, d, info)
  call begin_test('1x1')
  call print_array('a', a_r, 2)
  call print_array('d', d_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 5: 1x1 with negative real part
  a(1) = (-0.5d0, 0.4d0)
  d(1) = (0.0d0, 0.0d0)
  call zlaunhr_col_getrfnp2(1, 1, a, 1, d, info)
  call begin_test('1x1_neg')
  call print_array('a', a_r, 2)
  call print_array('d', d_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 6: 5x1 single column (N=1 branch)
  a = (0.0d0, 0.0d0)
  d = (0.0d0, 0.0d0)
  a(1) = (0.6d0, 0.2d0); a(2) = (0.3d0, -0.1d0); a(3) = (-0.2d0, 0.4d0)
  a(4) = (0.1d0, 0.3d0); a(5) = (-0.4d0, -0.2d0)
  call zlaunhr_col_getrfnp2(5, 1, a, 5, d, info)
  call begin_test('5x1')
  call print_array('a', a_r, 10)
  call print_array('d', d_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 7: 1x5 single row
  a = (0.0d0, 0.0d0)
  d = (0.0d0, 0.0d0)
  a(1) = (0.6d0, 0.2d0); a(2) = (0.3d0, -0.1d0); a(3) = (-0.2d0, 0.4d0)
  a(4) = (0.1d0, 0.3d0); a(5) = (-0.4d0, -0.2d0)
  call zlaunhr_col_getrfnp2(1, 5, a, 1, d, info)
  call begin_test('1x5')
  call print_array('a', a_r, 10)
  call print_array('d', d_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 8: 5x5 larger recursive case
  a = (0.0d0, 0.0d0)
  d = (0.0d0, 0.0d0)
  a(1)  = (0.50d0, 0.30d0); a(2)  = (0.20d0,-0.10d0); a(3)  = (-0.30d0, 0.20d0); a(4)  = (0.10d0, 0.40d0); a(5)  = (-0.20d0,-0.30d0)
  a(6)  = (-0.10d0, 0.40d0); a(7)  = (0.60d0,-0.20d0); a(8)  = (0.10d0, 0.30d0); a(9)  = (-0.20d0,-0.40d0); a(10) = (0.30d0, 0.10d0)
  a(11) = (0.20d0,-0.30d0); a(12) = (-0.40d0, 0.10d0); a(13) = (0.50d0, 0.20d0); a(14) = (0.30d0,-0.10d0); a(15) = (-0.10d0, 0.40d0)
  a(16) = (-0.30d0, 0.10d0); a(17) = (0.20d0, 0.30d0); a(18) = (-0.10d0,-0.40d0); a(19) = (0.60d0, 0.20d0); a(20) = (0.40d0,-0.10d0)
  a(21) = (0.40d0,-0.20d0); a(22) = (0.10d0, 0.30d0); a(23) = (-0.20d0, 0.40d0); a(24) = (-0.30d0,-0.10d0); a(25) = (0.50d0, 0.30d0)
  call zlaunhr_col_getrfnp2(5, 5, a, 5, d, info)
  call begin_test('5x5')
  call print_array('a', a_r, 50)
  call print_array('d', d_r, 10)
  call print_int('info', info)
  call end_test()
end program
