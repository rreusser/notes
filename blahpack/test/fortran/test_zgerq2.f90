program test_zgerq2
  use test_utils
  implicit none

  double precision :: a_r(72), tau_r(12), work_r(12)
  complex*16 :: a(36), tau(6), work(6)
  equivalence (a, a_r)
  equivalence (tau, tau_r)
  equivalence (work, work_r)
  integer :: info

  ! Test 1: 3x4 matrix (M < N) with complex entries
  a = (0.0d0, 0.0d0)
  a(1) = (2.0d0, 1.0d0); a(2) = (1.0d0, 0.0d0); a(3) = (3.0d0, -1.0d0)
  a(4) = (1.0d0, 2.0d0); a(5) = (4.0d0, 1.0d0); a(6) = (2.0d0, 0.0d0)
  a(7) = (3.0d0, 0.0d0); a(8) = (2.0d0, -1.0d0); a(9) = (5.0d0, 2.0d0)
  a(10) = (1.0d0, 1.0d0); a(11) = (3.0d0, 0.0d0); a(12) = (2.0d0, -2.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(3, 4, a, 3, tau, work, info)
  call begin_test('3x4')
  call print_array('a', a_r, 24)
  call print_array('tau', tau_r, 6)
  call print_int('info', info)
  call end_test()

  ! Test 2: 4x3 matrix (M > N) with complex entries
  a = (0.0d0, 0.0d0)
  a(1) = (2.0d0, 1.0d0); a(2) = (1.0d0, -1.0d0); a(3) = (3.0d0, 0.0d0); a(4) = (1.0d0, 2.0d0)
  a(5) = (1.0d0, 0.0d0); a(6) = (4.0d0, 1.0d0); a(7) = (2.0d0, -1.0d0); a(8) = (3.0d0, 0.0d0)
  a(9) = (3.0d0, 1.0d0); a(10) = (2.0d0, 0.0d0); a(11) = (5.0d0, -2.0d0); a(12) = (1.0d0, 1.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(4, 3, a, 4, tau, work, info)
  call begin_test('4x3')
  call print_array('a', a_r, 24)
  call print_array('tau', tau_r, 6)
  call print_int('info', info)
  call end_test()

  ! Test 3: 3x3 square matrix
  a = (0.0d0, 0.0d0)
  a(1) = (4.0d0, 1.0d0); a(2) = (1.0d0, 0.0d0); a(3) = (2.0d0, -1.0d0)
  a(4) = (1.0d0, -1.0d0); a(5) = (3.0d0, 2.0d0); a(6) = (1.0d0, 0.0d0)
  a(7) = (2.0d0, 0.0d0); a(8) = (1.0d0, 1.0d0); a(9) = (5.0d0, -2.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(3, 3, a, 3, tau, work, info)
  call begin_test('3x3')
  call print_array('a', a_r, 18)
  call print_array('tau', tau_r, 6)
  call print_int('info', info)
  call end_test()

  ! Test 4: 1x4 (single row)
  a = (0.0d0, 0.0d0)
  a(1) = (1.0d0, 2.0d0); a(2) = (2.0d0, -1.0d0); a(3) = (3.0d0, 0.0d0); a(4) = (4.0d0, 1.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(1, 4, a, 1, tau, work, info)
  call begin_test('1x4')
  call print_array('a', a_r, 8)
  call print_array('tau', tau_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 5: 3x1 (single column)
  a = (0.0d0, 0.0d0)
  a(1) = (2.0d0, 1.0d0)
  a(2) = (3.0d0, -1.0d0)
  a(3) = (4.0d0, 0.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(3, 1, a, 3, tau, work, info)
  call begin_test('3x1')
  call print_array('a', a_r, 6)
  call print_array('tau', tau_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 6: M=0 (quick return)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(0, 3, a, 1, tau, work, info)
  call begin_test('m_zero')
  call print_int('info', info)
  call end_test()

  ! Test 7: N=0 (quick return)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(3, 0, a, 3, tau, work, info)
  call begin_test('n_zero')
  call print_int('info', info)
  call end_test()

  ! Test 8: 1x1 matrix
  a(1) = (5.0d0, 3.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(1, 1, a, 1, tau, work, info)
  call begin_test('1x1')
  call print_array('a', a_r, 2)
  call print_array('tau', tau_r, 2)
  call print_int('info', info)
  call end_test()

  ! Test 9: 2x5 matrix (wide, M << N)
  a = (0.0d0, 0.0d0)
  a(1) = (1.0d0, 0.0d0); a(2) = (6.0d0, -1.0d0)
  a(3) = (2.0d0, 1.0d0); a(4) = (7.0d0, 0.0d0)
  a(5) = (3.0d0, -1.0d0); a(6) = (8.0d0, 2.0d0)
  a(7) = (4.0d0, 0.0d0); a(8) = (9.0d0, -1.0d0)
  a(9) = (5.0d0, 1.0d0); a(10) = (10.0d0, 0.0d0)
  tau = (0.0d0, 0.0d0)
  call ZGERQ2(2, 5, a, 2, tau, work, info)
  call begin_test('2x5')
  call print_array('a', a_r, 20)
  call print_array('tau', tau_r, 4)
  call print_int('info', info)
  call end_test()

end program
