program test_zspr
  use test_utils
  implicit none
  complex*16 :: ap(15), x(8), alpha
  double precision :: ap_r(30), x_r(16)
  equivalence (ap, ap_r)
  equivalence (x, x_r)

  ! Test 1: UPLO='U', N=3, alpha=(2,0), unit stride
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, 1.0d0); ap(3) = (4.0d0, -0.5d0)
  ap(4) = (3.0d0, -2.0d0); ap(5) = (2.0d0, 1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.5d0); x(2) = (2.0d0, -1.0d0); x(3) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('U', 3, alpha, x, 1, ap)
  call begin_test('upper_basic')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 2: UPLO='L', N=3, alpha=(2,0), unit stride
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, -1.0d0); ap(3) = (3.0d0, 2.0d0)
  ap(4) = (4.0d0, -0.5d0); ap(5) = (2.0d0, -1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.5d0); x(2) = (2.0d0, -1.0d0); x(3) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('L', 3, alpha, x, 1, ap)
  call begin_test('lower_basic')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 3: alpha=(0,0) (no-op)
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, 1.0d0); ap(3) = (4.0d0, -0.5d0)
  ap(4) = (3.0d0, -2.0d0); ap(5) = (2.0d0, 1.0d0); ap(6) = (5.0d0, 0.3d0)
  alpha = (0.0d0, 0.0d0)
  call zspr('U', 3, alpha, x, 1, ap)
  call begin_test('alpha_zero')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 4: N=0 quick return
  ap(1) = (99.0d0, 1.0d0)
  alpha = (1.0d0, 0.0d0)
  call zspr('U', 0, alpha, x, 1, ap)
  call begin_test('n_zero')
  call print_array('AP', ap_r, 2)
  call end_test()

  ! Test 5: N=1 scalar case, alpha=(1.5, 0.5)
  ap = (0.0d0, 0.0d0)
  ap(1) = (3.0d0, 1.0d0)
  x = (0.0d0, 0.0d0)
  x(1) = (2.0d0, 1.0d0)
  alpha = (1.5d0, 0.5d0)
  call zspr('U', 1, alpha, x, 1, ap)
  call begin_test('scalar')
  call print_array('AP', ap_r, 2)
  call end_test()

  ! Test 6: non-unit stride incx=2, upper
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, 1.0d0); ap(3) = (4.0d0, -0.5d0)
  ap(4) = (3.0d0, -2.0d0); ap(5) = (2.0d0, 1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.5d0); x(3) = (2.0d0, -1.0d0); x(5) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('U', 3, alpha, x, 2, ap)
  call begin_test('upper_stride_2')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 7: x has zero element (exercises skip branch)
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, 1.0d0); ap(3) = (4.0d0, -0.5d0)
  ap(4) = (3.0d0, -2.0d0); ap(5) = (2.0d0, 1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.5d0); x(2) = (0.0d0, 0.0d0); x(3) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('U', 3, alpha, x, 1, ap)
  call begin_test('zero_element')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 8: lower with non-unit stride
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, -1.0d0); ap(3) = (3.0d0, 2.0d0)
  ap(4) = (4.0d0, -0.5d0); ap(5) = (2.0d0, -1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.5d0); x(3) = (2.0d0, -1.0d0); x(5) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('L', 3, alpha, x, 2, ap)
  call begin_test('lower_stride_2')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 9: complex alpha with imaginary part, upper
  ap = (0.0d0, 0.0d0)
  ap(1) = (1.0d0, 0.0d0); ap(2) = (0.0d0, 0.0d0); ap(3) = (1.0d0, 0.0d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.0d0); x(2) = (0.0d0, 1.0d0)
  alpha = (1.0d0, 2.0d0)
  call zspr('U', 2, alpha, x, 1, ap)
  call begin_test('complex_alpha_upper')
  call print_array('AP', ap_r, 6)
  call end_test()

  ! Test 10: complex alpha with imaginary part, lower
  ap = (0.0d0, 0.0d0)
  ap(1) = (1.0d0, 0.0d0); ap(2) = (0.0d0, 0.0d0); ap(3) = (1.0d0, 0.0d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.0d0); x(2) = (0.0d0, 1.0d0)
  alpha = (1.0d0, 2.0d0)
  call zspr('L', 2, alpha, x, 1, ap)
  call begin_test('complex_alpha_lower')
  call print_array('AP', ap_r, 6)
  call end_test()

  ! Test 11: negative stride incx=-1, upper
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, 1.0d0); ap(3) = (4.0d0, -0.5d0)
  ap(4) = (3.0d0, -2.0d0); ap(5) = (2.0d0, 1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 0.5d0); x(2) = (2.0d0, -1.0d0); x(3) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('U', 3, alpha, x, -1, ap)
  call begin_test('upper_neg_stride')
  call print_array('AP', ap_r, 12)
  call end_test()

  ! Test 12: lower with zero element
  ap = (0.0d0, 0.0d0)
  ap(1) = (2.0d0, 0.5d0); ap(2) = (1.0d0, -1.0d0); ap(3) = (3.0d0, 2.0d0)
  ap(4) = (4.0d0, -0.5d0); ap(5) = (2.0d0, -1.0d0); ap(6) = (5.0d0, 0.3d0)
  x = (0.0d0, 0.0d0)
  x(1) = (0.0d0, 0.0d0); x(2) = (2.0d0, -1.0d0); x(3) = (3.0d0, 1.0d0)
  alpha = (2.0d0, 0.0d0)
  call zspr('L', 3, alpha, x, 1, ap)
  call begin_test('lower_zero_element')
  call print_array('AP', ap_r, 12)
  call end_test()

end program
