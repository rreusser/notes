program test_zlarfy
  use test_utils
  implicit none
  complex*16 :: C(25), v(5), WORK(5), tau
  double precision :: C_r(50), v_r(10), W_r(10)
  equivalence (C, C_r)
  equivalence (v, v_r)
  equivalence (WORK, W_r)

  ! Test 1: Upper, N=3, real tau=1.0, Hermitian C with complex off-diagonals
  C = (0.0d0, 0.0d0)
  C(1) = (4.0d0, 0.0d0)
  C(4) = (1.0d0, 2.0d0)
  C(5) = (5.0d0, 0.0d0)
  C(7) = (2.0d0, -1.0d0)
  C(8) = (3.0d0, 1.0d0)
  C(9) = (6.0d0, 0.0d0)
  ! corresponding lower (not referenced):
  C(2) = (1.0d0, -2.0d0)
  C(3) = (2.0d0, 1.0d0)
  C(6) = (3.0d0, -1.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (0.5d0, 0.25d0)
  v(3) = (0.25d0, -0.5d0)
  tau = (1.0d0, 0.0d0)
  call zlarfy('U', 3, v, 1, tau, C, 3, WORK)
  call begin_test('upper_3_real_tau')
  call print_array('C', C_r, 18)
  call end_test()

  ! Test 2: Lower, N=3, real tau=1.0
  C = (0.0d0, 0.0d0)
  C(1) = (4.0d0, 0.0d0)
  C(4) = (1.0d0, 2.0d0)
  C(5) = (5.0d0, 0.0d0)
  C(7) = (2.0d0, -1.0d0)
  C(8) = (3.0d0, 1.0d0)
  C(9) = (6.0d0, 0.0d0)
  C(2) = (1.0d0, -2.0d0)
  C(3) = (2.0d0, 1.0d0)
  C(6) = (3.0d0, -1.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (0.5d0, 0.25d0)
  v(3) = (0.25d0, -0.5d0)
  tau = (1.0d0, 0.0d0)
  call zlarfy('L', 3, v, 1, tau, C, 3, WORK)
  call begin_test('lower_3_real_tau')
  call print_array('C', C_r, 18)
  call end_test()

  ! Test 3: tau = 0 quick return
  C = (0.0d0, 0.0d0)
  C(1) = (4.0d0, 0.0d0)
  C(4) = (1.0d0, 2.0d0)
  C(5) = (5.0d0, 0.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (2.0d0, 3.0d0)
  tau = (0.0d0, 0.0d0)
  call zlarfy('U', 2, v, 1, tau, C, 2, WORK)
  call begin_test('tau_zero')
  call print_array('C', C_r, 8)
  call end_test()

  ! Test 4: Upper, N=2 with complex tau
  C = (0.0d0, 0.0d0)
  C(1) = (2.0d0, 0.0d0)
  C(3) = (1.0d0, 1.0d0)
  C(4) = (3.0d0, 0.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (0.5d0, 0.5d0)
  tau = (0.7d0, 0.3d0)
  call zlarfy('U', 2, v, 1, tau, C, 2, WORK)
  call begin_test('upper_2_complex_tau')
  call print_array('C', C_r, 8)
  call end_test()

  ! Test 5: Lower, N=2 with complex tau
  C = (0.0d0, 0.0d0)
  C(1) = (2.0d0, 0.0d0)
  C(2) = (1.0d0, -1.0d0)
  C(4) = (3.0d0, 0.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (0.5d0, 0.5d0)
  tau = (0.7d0, 0.3d0)
  call zlarfy('L', 2, v, 1, tau, C, 2, WORK)
  call begin_test('lower_2_complex_tau')
  call print_array('C', C_r, 8)
  call end_test()

  ! Test 6: Upper, N=5 with complex tau
  C = (0.0d0, 0.0d0)
  C(1)  = (10.0d0, 0.0d0)
  C(6)  = (1.0d0, 1.0d0);  C(7)  = (11.0d0, 0.0d0)
  C(11) = (2.0d0, -1.0d0); C(12) = (5.0d0, 2.0d0);  C(13) = (12.0d0, 0.0d0)
  C(16) = (3.0d0, 0.5d0);  C(17) = (6.0d0, -1.0d0); C(18) = (8.0d0, 1.5d0); C(19) = (13.0d0, 0.0d0)
  C(21) = (4.0d0, 1.0d0);  C(22) = (7.0d0, 0.5d0);  C(23) = (9.0d0, -0.5d0); C(24) = (1.5d0, 2.0d0); C(25) = (14.0d0, 0.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (0.4d0, 0.1d0)
  v(3) = (0.3d0, -0.2d0)
  v(4) = (0.2d0, 0.3d0)
  v(5) = (0.1d0, -0.1d0)
  tau = (0.6d0, 0.2d0)
  call zlarfy('U', 5, v, 1, tau, C, 5, WORK)
  call begin_test('upper_5_complex_tau')
  call print_array('C', C_r, 50)
  call end_test()

  ! Test 7: Lower, N=5 with complex tau
  C = (0.0d0, 0.0d0)
  C(1)  = (10.0d0, 0.0d0)
  C(2)  = (1.0d0, -1.0d0); C(3)  = (2.0d0, 1.0d0); C(4) = (3.0d0, -0.5d0); C(5) = (4.0d0, -1.0d0)
  C(7)  = (11.0d0, 0.0d0)
  C(8)  = (5.0d0, -2.0d0); C(9) = (6.0d0, 1.0d0); C(10) = (7.0d0, -0.5d0)
  C(13) = (12.0d0, 0.0d0)
  C(14) = (8.0d0, -1.5d0); C(15) = (9.0d0, 0.5d0)
  C(19) = (13.0d0, 0.0d0)
  C(20) = (1.5d0, -2.0d0)
  C(25) = (14.0d0, 0.0d0)
  v(1) = (1.0d0, 0.0d0)
  v(2) = (0.4d0, 0.1d0)
  v(3) = (0.3d0, -0.2d0)
  v(4) = (0.2d0, 0.3d0)
  v(5) = (0.1d0, -0.1d0)
  tau = (0.6d0, 0.2d0)
  call zlarfy('L', 5, v, 1, tau, C, 5, WORK)
  call begin_test('lower_5_complex_tau')
  call print_array('C', C_r, 50)
  call end_test()

  ! Test 8: N=1 edge case
  C(1) = (5.0d0, 0.0d0)
  v(1) = (1.0d0, 0.0d0)
  tau = (1.0d0, 0.0d0)
  call zlarfy('U', 1, v, 1, tau, C, 1, WORK)
  call begin_test('n_one')
  call print_array('C', C_r, 2)
  call end_test()

end program
