program test_zlacrt
  use test_utils
  implicit none
  complex*16 :: cx(10), cy(10)
  double precision :: cx_r(20), cy_r(20)
  equivalence (cx, cx_r)
  equivalence (cy, cy_r)
  complex*16 :: c, s
  integer :: n

  ! Test 1: basic, n=3, c=(0.6, 0.1), s=(0.8, 0.2), incx=1, incy=1
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 2.0d0)
  cx(2) = (3.0d0, 4.0d0)
  cx(3) = (5.0d0, 6.0d0)
  cy(1) = (7.0d0, 8.0d0)
  cy(2) = (9.0d0, 10.0d0)
  cy(3) = (11.0d0, 12.0d0)
  n = 3
  c = (0.6d0, 0.1d0)
  s = (0.8d0, 0.2d0)
  call zlacrt(n, cx, 1, cy, 1, c, s)
  call begin_test('basic')
  call print_array('cx', cx_r, 2*n)
  call print_array('cy', cy_r, 2*n)
  call end_test()

  ! Test 2: n=0 (no-op)
  cx(1) = (1.0d0, 2.0d0)
  cy(1) = (3.0d0, 4.0d0)
  c = (0.6d0, 0.1d0)
  s = (0.8d0, 0.2d0)
  call zlacrt(0, cx, 1, cy, 1, c, s)
  call begin_test('n_zero')
  call print_array('cx', cx_r, 2)
  call print_array('cy', cy_r, 2)
  call end_test()

  ! Test 3: n=1
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (2.0d0, 3.0d0)
  cy(1) = (4.0d0, 5.0d0)
  n = 1
  c = (1.0d0, 0.0d0)
  s = (0.0d0, 1.0d0)
  call zlacrt(n, cx, 1, cy, 1, c, s)
  call begin_test('n_one')
  call print_array('cx', cx_r, 2)
  call print_array('cy', cy_r, 2)
  call end_test()

  ! Test 4: identity rotation c=(1,0), s=(0,0)
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 2.0d0)
  cx(2) = (3.0d0, 4.0d0)
  cy(1) = (5.0d0, 6.0d0)
  cy(2) = (7.0d0, 8.0d0)
  n = 2
  c = (1.0d0, 0.0d0)
  s = (0.0d0, 0.0d0)
  call zlacrt(n, cx, 1, cy, 1, c, s)
  call begin_test('identity')
  call print_array('cx', cx_r, 2*n)
  call print_array('cy', cy_r, 2*n)
  call end_test()

  ! Test 5: swap c=(0,0), s=(1,0) => cx=cy, cy=-cx
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 2.0d0)
  cx(2) = (3.0d0, 4.0d0)
  cy(1) = (5.0d0, 6.0d0)
  cy(2) = (7.0d0, 8.0d0)
  n = 2
  c = (0.0d0, 0.0d0)
  s = (1.0d0, 0.0d0)
  call zlacrt(n, cx, 1, cy, 1, c, s)
  call begin_test('swap')
  call print_array('cx', cx_r, 2*n)
  call print_array('cy', cy_r, 2*n)
  call end_test()

  ! Test 6: non-unit strides, incx=2, incy=2
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 2.0d0)
  cx(2) = (99.0d0, 99.0d0)
  cx(3) = (3.0d0, 4.0d0)
  cx(4) = (99.0d0, 99.0d0)
  cx(5) = (5.0d0, 6.0d0)
  cy(1) = (7.0d0, 8.0d0)
  cy(2) = (88.0d0, 88.0d0)
  cy(3) = (9.0d0, 10.0d0)
  cy(4) = (88.0d0, 88.0d0)
  cy(5) = (11.0d0, 12.0d0)
  n = 3
  c = (0.6d0, 0.1d0)
  s = (0.8d0, 0.2d0)
  call zlacrt(n, cx, 2, cy, 2, c, s)
  call begin_test('stride')
  call print_array('cx', cx_r, 10)
  call print_array('cy', cy_r, 10)
  call end_test()

  ! Test 7: negative stride (incx=-1, incy=1)
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 2.0d0)
  cx(2) = (3.0d0, 4.0d0)
  cx(3) = (5.0d0, 6.0d0)
  cy(1) = (7.0d0, 8.0d0)
  cy(2) = (9.0d0, 10.0d0)
  cy(3) = (11.0d0, 12.0d0)
  n = 3
  c = (0.6d0, 0.1d0)
  s = (0.8d0, 0.2d0)
  call zlacrt(n, cx, -1, cy, 1, c, s)
  call begin_test('neg_stride')
  call print_array('cx', cx_r, 2*n)
  call print_array('cy', cy_r, 2*n)
  call end_test()

  ! Test 8: purely imaginary c and s
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 0.0d0)
  cx(2) = (0.0d0, 1.0d0)
  cy(1) = (1.0d0, 0.0d0)
  cy(2) = (0.0d0, 1.0d0)
  n = 2
  c = (0.0d0, 1.0d0)
  s = (0.0d0, 1.0d0)
  call zlacrt(n, cx, 1, cy, 1, c, s)
  call begin_test('imag_cs')
  call print_array('cx', cx_r, 2*n)
  call print_array('cy', cy_r, 2*n)
  call end_test()

  ! Test 9: mixed strides incx=2, incy=-1
  cx = (0.0d0, 0.0d0)
  cy = (0.0d0, 0.0d0)
  cx(1) = (1.0d0, 1.0d0)
  cx(2) = (99.0d0, 99.0d0)
  cx(3) = (2.0d0, 2.0d0)
  cy(1) = (3.0d0, 3.0d0)
  cy(2) = (4.0d0, 4.0d0)
  n = 2
  c = (0.5d0, 0.5d0)
  s = (0.5d0, -0.5d0)
  call zlacrt(n, cx, 2, cy, -1, c, s)
  call begin_test('mixed_stride')
  call print_array('cx', cx_r, 6)
  call print_array('cy', cy_r, 4)
  call end_test()

end program
