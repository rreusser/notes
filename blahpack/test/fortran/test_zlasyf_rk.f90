program test_zlasyf_rk
  use test_utils
  implicit none

  complex*16 :: A6(6, 6), W6(6, 3), e6(6)
  double precision :: A6_r(72), e6_r(12)
  equivalence (A6, A6_r)
  equivalence (e6, e6_r)
  complex*16 :: A4(4, 4), W4(4, 4), e4(4)
  double precision :: A4_r(32), e4_r(8)
  equivalence (A4, A4_r)
  equivalence (e4, e4_r)
  integer :: IPIV(6), INFO, kb

  ! Test 1: Lower, 6x6 complex symmetric, NB=3 (partial panel)
  A6 = (0.0d0, 0.0d0)
  A6(1,1) = (0.01d0, 0.01d0)
  A6(2,1) = (5.0d0, -1.0d0)
  A6(2,2) = (0.02d0, -0.02d0)
  A6(3,1) = (1.0d0, 1.0d0)
  A6(3,2) = (2.0d0, -1.0d0)
  A6(3,3) = (8.0d0, 0.5d0)
  A6(4,1) = (0.5d0, -0.5d0)
  A6(4,2) = (1.0d0, 1.0d0)
  A6(4,3) = (3.0d0, 0.3d0)
  A6(4,4) = (7.0d0, -0.2d0)
  A6(5,1) = (2.0d0, 0.2d0)
  A6(5,2) = (1.5d0, -0.5d0)
  A6(5,3) = (0.0d0, 2.0d0)
  A6(5,4) = (1.0d0, 0.5d0)
  A6(5,5) = (6.0d0, 0.1d0)
  A6(6,1) = (1.0d0, -1.0d0)
  A6(6,2) = (0.0d0, -3.0d0)
  A6(6,3) = (1.0d0, 0.4d0)
  A6(6,4) = (2.0d0, -2.0d0)
  A6(6,5) = (0.5d0, 1.0d0)
  A6(6,6) = (5.0d0, 0.3d0)
  e6 = (0.0d0, 0.0d0)
  IPIV = 0
  call ZLASYF_RK('L', 6, 3, kb, A6, 6, e6, IPIV, W6, 6, INFO)
  call begin_test('lower_6x6_nb3')
  call print_int('n', 6)
  call print_int('nb', 3)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A6_r, 72)
  call print_array('e', e6_r, 12)
  call print_int_array('ipiv', IPIV, 6)
  call end_test()

  ! Test 2: Upper, 6x6 complex symmetric, NB=3
  A6 = (0.0d0, 0.0d0)
  A6(1,1) = (0.01d0, 0.01d0)
  A6(1,2) = (5.0d0, -1.0d0)
  A6(2,2) = (0.02d0, -0.02d0)
  A6(1,3) = (1.0d0, 1.0d0)
  A6(2,3) = (2.0d0, -1.0d0)
  A6(3,3) = (8.0d0, 0.5d0)
  A6(1,4) = (0.5d0, -0.5d0)
  A6(2,4) = (1.0d0, 1.0d0)
  A6(3,4) = (3.0d0, 0.3d0)
  A6(4,4) = (7.0d0, -0.2d0)
  A6(1,5) = (2.0d0, 0.2d0)
  A6(2,5) = (1.5d0, -0.5d0)
  A6(3,5) = (0.0d0, 2.0d0)
  A6(4,5) = (1.0d0, 0.5d0)
  A6(5,5) = (6.0d0, 0.1d0)
  A6(1,6) = (1.0d0, -1.0d0)
  A6(2,6) = (0.0d0, -3.0d0)
  A6(3,6) = (1.0d0, 0.4d0)
  A6(4,6) = (2.0d0, -2.0d0)
  A6(5,6) = (0.5d0, 1.0d0)
  A6(6,6) = (5.0d0, 0.3d0)
  e6 = (0.0d0, 0.0d0)
  IPIV = 0
  call ZLASYF_RK('U', 6, 3, kb, A6, 6, e6, IPIV, W6, 6, INFO)
  call begin_test('upper_6x6_nb3')
  call print_int('n', 6)
  call print_int('nb', 3)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A6_r, 72)
  call print_array('e', e6_r, 12)
  call print_int_array('ipiv', IPIV, 6)
  call end_test()

  ! Test 3: Lower, 4x4 full factorization
  A4 = (0.0d0, 0.0d0)
  A4(1,1) = (4.0d0, 0.2d0)
  A4(2,1) = (1.0d0, 0.5d0)
  A4(2,2) = (3.0d0, -0.1d0)
  A4(3,1) = (2.0d0, -1.0d0)
  A4(3,2) = (0.5d0, -0.2d0)
  A4(3,3) = (5.0d0, 0.3d0)
  A4(4,1) = (0.5d0, 0.1d0)
  A4(4,2) = (1.0d0, 0.3d0)
  A4(4,3) = (0.2d0, -0.4d0)
  A4(4,4) = (6.0d0, -0.25d0)
  e4 = (0.0d0, 0.0d0)
  IPIV = 0
  call ZLASYF_RK('L', 4, 8, kb, A4, 4, e4, IPIV, W4, 4, INFO)
  call begin_test('lower_4x4_full_nb8')
  call print_int('n', 4)
  call print_int('nb', 8)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A4_r, 32)
  call print_array('e', e4_r, 8)
  call print_int_array('ipiv', IPIV, 4)
  call end_test()

  ! Test 4: Upper, 4x4 full factorization
  A4 = (0.0d0, 0.0d0)
  A4(1,1) = (4.0d0, 0.2d0)
  A4(1,2) = (1.0d0, 0.5d0)
  A4(2,2) = (3.0d0, -0.1d0)
  A4(1,3) = (2.0d0, -1.0d0)
  A4(2,3) = (0.5d0, -0.2d0)
  A4(3,3) = (5.0d0, 0.3d0)
  A4(1,4) = (0.5d0, 0.1d0)
  A4(2,4) = (1.0d0, 0.3d0)
  A4(3,4) = (0.2d0, -0.4d0)
  A4(4,4) = (6.0d0, -0.25d0)
  e4 = (0.0d0, 0.0d0)
  IPIV = 0
  call ZLASYF_RK('U', 4, 8, kb, A4, 4, e4, IPIV, W4, 4, INFO)
  call begin_test('upper_4x4_full_nb8')
  call print_int('n', 4)
  call print_int('nb', 8)
  call print_int('kb', kb)
  call print_int('info', INFO)
  call print_array('A', A4_r, 32)
  call print_array('e', e4_r, 8)
  call print_int_array('ipiv', IPIV, 4)
  call end_test()

end program
