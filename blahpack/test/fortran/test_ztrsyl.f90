  B3(1,1) = (4.0d0, -0.2d0); B3(1,2) = (0.5d0, 0.3d0); B3(1,3) = (0.2d0, -0.1d0)
  B3(2,2) = (5.0d0, 0.4d0); B3(2,3) = (0.3d0, 0.2d0)
