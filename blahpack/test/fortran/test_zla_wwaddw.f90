program test_zla_wwaddw
  use test_utils
  implicit none
  complex*16 :: x(10), y(10), w(10)
  double precision :: x_r(20), y_r(20)
  equivalence (x, x_r)
  equivalence (y, y_r)
  integer :: n

  ! Test 1: basic, n=5
  n = 5
  x = (0.0d0, 0.0d0)
  y = (0.0d0, 0.0d0)
  w = (0.0d0, 0.0d0)
  x(1) = (1.0d0, 2.0d0)
  x(2) = (3.0d0, 4.0d0)
  x(3) = (5.0d0, 6.0d0)
  x(4) = (7.0d0, 8.0d0)
  x(5) = (9.0d0, 10.0d0)
  y(1) = (0.1d0, 0.2d0)
  y(2) = (0.3d0, 0.4d0)
  y(3) = (0.5d0, 0.6d0)
  y(4) = (0.7d0, 0.8d0)
  y(5) = (0.9d0, 1.0d0)
  w(1) = (10.0d0, 20.0d0)
  w(2) = (30.0d0, 40.0d0)
  w(3) = (50.0d0, 60.0d0)
  w(4) = (70.0d0, 80.0d0)
  w(5) = (90.0d0, 100.0d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('basic')
  call print_array('x', x_r, 2*n)
  call print_array('y', y_r, 2*n)
  call end_test()

  ! Test 2: n=0 (quick return, arrays unchanged)
  n = 0
  x(1) = (1.0d0, 2.0d0)
  x(2) = (3.0d0, 4.0d0)
  y(1) = (0.1d0, 0.2d0)
  y(2) = (0.3d0, 0.4d0)
  w(1) = (10.0d0, 20.0d0)
  w(2) = (30.0d0, 40.0d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('n_zero')
  call print_array('x', x_r, 4)
  call print_array('y', y_r, 4)
  call end_test()

  ! Test 3: n=1
  n = 1
  x(1) = (1.0d0, 2.0d0)
  y(1) = (0.1d0, 0.2d0)
  w(1) = (10.0d0, 20.0d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('n_one')
  call print_array('x', x_r, 2*n)
  call print_array('y', y_r, 2*n)
  call end_test()

  ! Test 4: negative values
  n = 4
  x(1) = (-1.0d0, -2.0d0)
  x(2) = (-3.0d0, -4.0d0)
  x(3) = (-5.0d0, -6.0d0)
  x(4) = (-7.0d0, -8.0d0)
  y(1) = (0.01d0, -0.02d0)
  y(2) = (0.03d0, -0.04d0)
  y(3) = (0.05d0, -0.06d0)
  y(4) = (0.07d0, -0.08d0)
  w(1) = (0.5d0, -0.5d0)
  w(2) = (-0.5d0, 0.5d0)
  w(3) = (1.5d0, -1.5d0)
  w(4) = (-1.5d0, 1.5d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('negative')
  call print_array('x', x_r, 2*n)
  call print_array('y', y_r, 2*n)
  call end_test()

  ! Test 5: large values (test precision behavior)
  n = 3
  x(1) = (1.0d15, 2.0d15)
  x(2) = (3.0d15, 4.0d15)
  x(3) = (5.0d15, 6.0d15)
  y(1) = (1.0d0, 2.0d0)
  y(2) = (3.0d0, 4.0d0)
  y(3) = (5.0d0, 6.0d0)
  w(1) = (1.0d0, 2.0d0)
  w(2) = (3.0d0, 4.0d0)
  w(3) = (5.0d0, 6.0d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('large_values')
  call print_array('x', x_r, 2*n)
  call print_array('y', y_r, 2*n)
  call end_test()

  ! Test 6: zeros
  n = 3
  x(1) = (0.0d0, 0.0d0)
  x(2) = (0.0d0, 0.0d0)
  x(3) = (0.0d0, 0.0d0)
  y(1) = (0.0d0, 0.0d0)
  y(2) = (0.0d0, 0.0d0)
  y(3) = (0.0d0, 0.0d0)
  w(1) = (0.0d0, 0.0d0)
  w(2) = (0.0d0, 0.0d0)
  w(3) = (0.0d0, 0.0d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('zeros')
  call print_array('x', x_r, 2*n)
  call print_array('y', y_r, 2*n)
  call end_test()

  ! Test 7: purely imaginary
  n = 3
  x(1) = (0.0d0, 1.0d0)
  x(2) = (0.0d0, 3.0d0)
  x(3) = (0.0d0, 5.0d0)
  y(1) = (0.0d0, 0.1d0)
  y(2) = (0.0d0, 0.3d0)
  y(3) = (0.0d0, 0.5d0)
  w(1) = (0.0d0, 10.0d0)
  w(2) = (0.0d0, 30.0d0)
  w(3) = (0.0d0, 50.0d0)
  call ZLA_WWADDW(n, x, y, w)
  call begin_test('purely_imaginary')
  call print_array('x', x_r, 2*n)
  call print_array('y', y_r, 2*n)
  call end_test()

end program
