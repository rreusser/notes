'use strict';

/* eslint-disable max-len */

// Code generator for register-tiled NN dgemm micro-kernels.
// Emits variants/gen-{MR}x{NR}.js: an MRxNR register tile (dot-product form,
// C accumulated in registers across K) for contiguous column-major NN, with
// scalar dot-product cleanup for edge rows/cols. Generating these mechanically
// lets us sweep tile geometry without hand-unrolling (and hand-bug) each size.

var fs = require( 'fs' );
var path = require( 'path' );

function gen( MR, NR ) {
	var L = [];
	var r, c;
	function w( s ) { L.push( s ); }

	w( "'use strict';" );
	w( '' );
	w( '/* eslint-disable */' );
	w( '' );
	w( '// AUTO-GENERATED by gen-tile.js. Tile = ' + MR + 'x' + NR + ' (MR x NR). Do not edit by hand.' );
	w( '' );
	w( "var ref = require( './v0-reference.js' );" );
	w( '' );
	w( 'function dgemm( transa, transb, M, N, K, alpha, A, sa1, sa2, oa, B, sb1, sb2, ob, beta, C, sc1, sc2, oc ) {' );
	// Declare hot register operands FIRST (accumulators, then A/B operands).
	// V8 register allocation is sensitive to declaration order; giving the
	// accumulators priority measurably improves codegen for these kernels.
	var accs = [];
	for ( c = 0; c < NR; c++ ) { for ( r = 0; r < MR; r++ ) { accs.push( 'c'+r+'_'+c ); } }
	w( '\tvar ' + accs.join( ', ' ) + ';' );
	var as = []; for ( r = 0; r < MR; r++ ) { as.push( 'a'+r ); }
	var bs = []; for ( c = 0; c < NR; c++ ) { bs.push( 'b'+c ); }
	var pbs = []; for ( c = 0; c < NR; c++ ) { pbs.push( 'pb'+c ); }
	w( '\tvar ' + as.concat( bs ).join( ', ' ) + ';' );
	w( '\tvar ' + pbs.join( ', ' ) + ';' );
	w( '\tvar i, j, l, jj, ii, mb, nb, temp, pa, pb, pc, paBase;' );
	w( '' );
	w( '\tif ( M === 0 || N === 0 || ( ( alpha === 0.0 || K === 0 ) && beta === 1.0 ) ) {' );
	w( '\t\treturn C;' );
	w( '\t}' );
	w( "\tif ( transa !== 'no-transpose' || transb !== 'no-transpose' || sa1 !== 1 || sc1 !== 1 || alpha === 0.0 ) {" );
	w( '\t\treturn ref( transa, transb, M, N, K, alpha, A, sa1, sa2, oa, B, sb1, sb2, ob, beta, C, sc1, sc2, oc );' );
	w( '\t}' );
	w( '\tmb = M - ( M % ' + MR + ' );' );
	w( '\tnb = N - ( N % ' + NR + ' );' );
	w( '\tfor ( j = 0; j < nb; j += ' + NR + ' ) {' );
	w( '\t\tpb0 = ob + ( j * sb2 );' );
	for ( c = 1; c < NR; c++ ) { w( '\t\tpb'+c+' = pb'+(c-1)+' + sb2;' ); }
	w( '\t\tfor ( i = 0; i < mb; i += ' + MR + ' ) {' );
	// zero accumulators:
	w( '\t\t\t' + accs.map( function ( a ) { return a+' = 0.0;'; } ).join( ' ' ) );
	w( '\t\t\tfor ( l = 0; l < K; l++ ) {' );
	w( '\t\t\t\tpaBase = oa + i + ( l * sa2 );' );
	for ( r = 0; r < MR; r++ ) { w( '\t\t\t\ta'+r+' = A[ paBase' + (r? ' + '+r : '') + ' ];' ); }
	for ( c = 0; c < NR; c++ ) { w( '\t\t\t\tb'+c+' = B[ pb'+c+' + ( l * sb1 ) ];' ); }
	for ( c = 0; c < NR; c++ ) {
		var fmas = [];
		for ( r = 0; r < MR; r++ ) { fmas.push( 'c'+r+'_'+c+' += a'+r+' * b'+c+';' ); }
		w( '\t\t\t\t' + fmas.join( ' ' ) );
	}
	w( '\t\t\t}' );
	// write-back:
	w( '\t\t\tpc = oc + i + ( j * sc2 );' );
	w( '\t\t\tif ( beta === 0.0 ) {' );
	for ( c = 0; c < NR; c++ ) {
		var asg = [];
		for ( r = 0; r < MR; r++ ) { asg.push( 'C[ pc'+(r? ' + '+r:'')+' ] = alpha * c'+r+'_'+c+';' ); }
		w( '\t\t\t\t' + asg.join( ' ' ) + ( c < NR-1 ? ' pc += sc2;' : '' ) );
	}
	w( '\t\t\t} else {' );
	for ( c = 0; c < NR; c++ ) {
		var asg2 = [];
		for ( r = 0; r < MR; r++ ) { var idx = 'pc'+(r? ' + '+r:''); asg2.push( 'C[ '+idx+' ] = ( alpha * c'+r+'_'+c+' ) + ( beta * C[ '+idx+' ] );' ); }
		w( '\t\t\t\t' + asg2.join( ' ' ) + ( c < NR-1 ? ' pc += sc2;' : '' ) );
	}
	w( '\t\t\t}' );
	w( '\t\t}' );
	w( '\t}' );
	// edge cleanup (scalar dot products):
	w( '\tfor ( jj = nb; jj < N; jj++ ) {' );
	w( '\t\tpb = ob + ( jj * sb2 );' );
	w( '\t\tfor ( ii = 0; ii < M; ii++ ) {' );
	w( '\t\t\ttemp = 0.0; pa = oa + ii;' );
	w( '\t\t\tfor ( l = 0; l < K; l++ ) { temp += A[ pa + ( l * sa2 ) ] * B[ pb + ( l * sb1 ) ]; }' );
	w( '\t\t\tpc = oc + ii + ( jj * sc2 );' );
	w( '\t\t\tC[ pc ] = ( beta === 0.0 ) ? alpha*temp : ( alpha*temp ) + ( beta*C[ pc ] );' );
	w( '\t\t}' );
	w( '\t}' );
	w( '\tfor ( jj = 0; jj < nb; jj++ ) {' );
	w( '\t\tpb = ob + ( jj * sb2 );' );
	w( '\t\tfor ( ii = mb; ii < M; ii++ ) {' );
	w( '\t\t\ttemp = 0.0; pa = oa + ii;' );
	w( '\t\t\tfor ( l = 0; l < K; l++ ) { temp += A[ pa + ( l * sa2 ) ] * B[ pb + ( l * sb1 ) ]; }' );
	w( '\t\t\tpc = oc + ii + ( jj * sc2 );' );
	w( '\t\t\tC[ pc ] = ( beta === 0.0 ) ? alpha*temp : ( alpha*temp ) + ( beta*C[ pc ] );' );
	w( '\t\t}' );
	w( '\t}' );
	w( '\treturn C;' );
	w( '}' );
	w( '' );
	w( 'module.exports = dgemm;' );
	w( '' );
	return L.join( '\n' );
}

// CLI: node gen-tile.js MRxNR [MRxNR ...]
var specs = process.argv.slice( 2 );
if ( specs.length === 0 ) { specs = [ '2x2','2x4','4x2','4x4','4x6','6x4','8x2','2x8','8x4','4x8','6x6' ]; }
specs.forEach( function ( spec ) {
	var m = spec.split( 'x' ).map( Number );
	var code = gen( m[0], m[1] );
	var file = path.join( __dirname, 'variants', 'gen-' + m[0] + 'x' + m[1] + '.js' );
	fs.writeFileSync( file, code );
	console.log( 'wrote ' + file );
} );
